export const filenameDate = (name: string): any => {
    const parsedName = name.replace('video', '').replace('.mkv', '').replace('.mp4', '')

    const [day, time] = parsedName.split('_')

    const parsedtime = time.split('-').slice(0, -1).join('-')

    return dayjs(`${day} ${parsedtime}`, 'YY-MM-DD HH-mm-ss')
}

files.sort((a, b) => filenameDate(a).isAfter(filenameDate(b)) ? 1 : -1)
